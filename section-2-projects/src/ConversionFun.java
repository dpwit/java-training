public class ConversionFun {
    public static void main (String[] args) {
        double myDouble = 3.14; // a real number is a double data type. (like a bucket)
        float myFloat = 3.14f; // the f at the end changes the real number from a double data type. Narrowing or lossy conversion (plastic cup)
        double yourDouble = myFloat; //Widening/lossless conversion.

    }; //end main
}
